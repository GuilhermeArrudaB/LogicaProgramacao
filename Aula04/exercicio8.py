"""
Faça um algoritmo que leia valores para as variáveis a, b e c e mostre o resultado da seguinte
expressão:
( a – b ) * c

"""

a = int(input('Digite o valor de A:'))
b = int(input('Digite o valor de B:'))
c = int(input('Digite o valor de C:'))

res = (a-b)*c

print('A resposta é:', res)

